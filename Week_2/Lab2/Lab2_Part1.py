"""
The distance that a car travels can be calculated with the following formula: distance = speed * time

A car is traveling at 55 miles per hour. Write a program that displays the following:

Distance traveled by the car in 5 hours
Distance traveled by the car in 8 hours
"""

# 5 Hours
time = 5

speed = 55

distance = speed * time

print("Distance for 5 hours is: ", distance)

# 8 Hours
time = 8

speed = 55

distance = speed * time

print("Distance for 8 hours is: ", distance)
